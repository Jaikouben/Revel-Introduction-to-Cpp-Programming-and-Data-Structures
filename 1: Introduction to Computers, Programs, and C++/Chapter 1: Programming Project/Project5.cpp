/*
(Physics: acceleration)
Average acceleration is defined as the change of velocity divided by the time taken to make the change, as shown in the following formula:
a = (v1 - v0) / t
Here, v0 is the starting velocity in meters/second, v1 is the ending velocity in meters/second, and t is the time span in seconds. Assume v0 is 5.6, v1 is 10.5, and t is 0.5, and write the code to display the average acceleration. 
*/

#include <iostream>
using namespace std;

int main()
{
    cout << (10.5 - 5.6) / 0.5;
}

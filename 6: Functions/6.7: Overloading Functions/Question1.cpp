/*
Write the definitions for three functions named max.
Each receives two parameters of the same type and returns the larger of the two values.
Define one of these functions to apply to type double, another to type int, and a third to type char.
*/

double max(double a, double b)
{
    if (a > b)
        return a;
    else
        return b;
}

int max(int a, int b)
{
    if (a > b)
        return a;
    else
        return b;
}

char max(char a, char b)
{
    if (a > b)
        return a;
    else
        return b;
}

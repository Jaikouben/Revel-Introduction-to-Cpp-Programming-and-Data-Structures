/*
Clunker Motors Inc. is recalling all vehicles from model years 2001-2006. A bool variable named recalled has been declared.
Given an int variable modelYear, write a statement that assigns true to recalled if the value of modelYear falls within the recall range and assigns false otherwise.
Do not use an if statement in this exercise!
*/

if (modelYear >= 2001 && modelYear <= 2006)
    recalled = true;
else
    recalled = false;
